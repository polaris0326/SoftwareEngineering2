import blservice.billblservice.BillBLS_Approve_Com;

public class BillBLS_Approve_Com_Driver{
	public void drive(BillBLS_Approve_Com billBLS_Approve_Com){
		if(billBLS_Approve_Com.approveComCorrect("id")){
			System.out.println("approve ComCorrect successfully");
		}
		if(billBLS_Approve_Com.approvePresent("id")){
			System.out.println("approve Present successfully");
		}
		billBLS_Approve_Com.approveSetWarningLine("id");
	}
	

}
