package dataservice_driver.strategydataservice_driver;

import dataservice.strategydataservice.StrategyDataService_ClientStrategy;

public class StrategyDataService_ClientStrategy_Driver{
	public void drive(StrategyDataService_ClientStrategy strategyDataService_ClientStrategy){
		if(strategyDataService_ClientStrategy.getClientRebateStrategy().add(null)){
			System.out.println("get ClientRebateStrategy successfully");
		}
		if(strategyDataService_ClientStrategy.getClientVoucherStrategy().add(null)){
			System.out.println("get ClientVoucherStrategy successfully");
		}
		strategyDataService_ClientStrategy.insertClientRebateStrategy(null);
		strategyDataService_ClientStrategy.insertClientVoucherStrategy(null);
	}
	

}
