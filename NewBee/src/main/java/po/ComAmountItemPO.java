package po;

import vo.ComAmountItemVO;

public class ComAmountItemPO {
	
	public ComAmountItemPO(String id, int amount) {
		super();
		this.id = id;
		this.amount = amount;
	}
	public ComAmountItemVO creatComAmountItemVO(){
		return new ComAmountItemVO(id, amount);
	}
	
	String id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}

	int amount;

}
